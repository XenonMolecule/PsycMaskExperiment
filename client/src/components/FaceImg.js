    const sad = [Sad01, Sad02, Sad03, Sad04, Sad05, Sad06, Sad07, Sad08, Sad09, Sad10, Sad11, Sad12];
    const sad_mask = [SadMask01, SadMask02, SadMask03, SadMask04, SadMask05, SadMask06, SadMask07,
        SadMask08, SadMask09, SadMask10, SadMask11, SadMask12];

    let actual_arr = happy;
    switch(emotion) {
        case "angry":
            actual_arr = mask ? angry_mask : angry;
            break;
        case "confused":
            actual_arr = mask ? confused_mask : confused;
            break;
        case "happy":
            actual_arr = mask ? happy_mask : happy;
            break;
        case "sad":
            actual_arr = mask ? sad_mask : sad;
            break;
    }

    const image = actual_arr[num];



    return (
        <img src={image}/>
    );
